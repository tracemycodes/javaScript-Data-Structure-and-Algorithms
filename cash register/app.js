// converted all prices to integers for error management
const currencyUnit = {
  PENNY: 1,
  NICKEL: 5,
  DIME: 10,
  QUARTER: 25,
  ONE: 100,
  FIVE: 500,
  TEN: 1000,
  TWENTY: 2000,
  'ONE HUNDRED': 10000,
};


function checkCashRegister(price, cash, cid) {
  let changeSum = (cash * 100) - (price * 100);
  let change = [];
  let status = '';


  let crSum = 0;

  // filter to remove all current price denominations without funds
  let filterCid = cid.filter((cash) => cash[1] !== 0).reverse();

  filterCid.forEach((element) => {
    let currPrice = element[0];
    let multiAmt = element[1] * 100;
    crSum += multiAmt;
    let amount = 0;

    while (changeSum >= currencyUnit[currPrice] && crSum > 0 && amount < multiAmt) {
      amount += currencyUnit[currPrice];
      changeSum -= currencyUnit[currPrice];
      crSum -= currencyUnit[currPrice];
    }

    if (amount !== 0) {
      change.push([currPrice, amount / 100]);
    }
  });


  if (changeSum > 0) {
     status  = "INSUFFICIENT_FUNDS";
     change = []
  } else if (changeSum == 0 && changeSum == crSum) {
    status = "CLOSED";
    change = cid;
  } else {
    status = "OPEN"
  }

  console.log(status, change);

  return {
    status: status,
    change: change
  }
}

